#include <unistd.h>

void	ft_is_negative(int n)
{
	int	negative;
	int	positive;

	negative = 78;
	positive = 80;
	if (n < 0)
	{
		write(1, &negative, 1);
	}
	else
	{
		write(1, &positive, 1);
	}
}
