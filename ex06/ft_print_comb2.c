#include <unistd.h>

void	ft_print_comb2(void)
{
	char	i;
	char	j;

	i = 48;
	while (i < 57)
	{
		j = i + 1;
		while (j <= 57)
		{
			write(1, &i, 1);
			write(1, " ", 1);
			write(1, &j, 1);
			write(1, ", ", 2);
			j++;
		}
		i++;
	}
}
int	main(void)
{
	ft_print_comb2();
}
